/* ==========================================================
   LORE.JS — процедурные "летописные" описания карт.
   Детерминированы по id карты (одна и та же карта — всегда
   один и тот же текст), собраны из тематических фрагментов
   под каждую стихию + редкость, чтобы каждая карта ощущалась
   как часть общего мрачного мира FoilDex.
   ========================================================== */

const CardLore = (() => {

  function makeRng(seedStr) {
    let h = 2166136261 ^ seedStr.length;
    for (let i = 0; i < seedStr.length; i++) {
      h = Math.imul(h ^ seedStr.charCodeAt(i), 16777619);
    }
    let a = h >>> 0;
    return function rng() {
      a += 0x6D2B79F5;
      let t = a;
      t = Math.imul(t ^ (t >>> 15), t | 1);
      t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
      return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
    };
  }
  const pick = (rng, arr) => arr[Math.floor(rng() * arr.length)];

  const TEMPLATES = {
    fire: [
      n => `${n} рождается там, где пепел ещё помнит форму пламени.`,
      n => `Говорят, ${n} не боится огня — потому что сам сплетён из углей.`,
      n => `След ${n} на камне остаётся выжженным на века.`,
      n => `${n} спит в жерле потухшего вулкана и просыпается от чужих шагов.`,
      n => `Там, где проходит ${n}, трава больше не растёт — только зола.`,
    ],
    water: [
      n => `${n} помнит имена всех, кто утонул в его владениях.`,
      n => `В штиль ${n} невидим — его выдаёт лишь холод в воздухе.`,
      n => `Рыбаки оставляют дары на берегу, чтобы ${n} прошёл мимо.`,
      n => `${n} движется бесшумно, как течение под коркой льда.`,
      n => `Говорят, ${n} старше самой реки, в которой он живёт.`,
    ],
    nature: [
      n => `${n} прорастает сквозь руины забытых храмов.`,
      n => `Корни ${n} тянутся глубже, чем помнит сама земля.`,
      n => `${n} не нападает первым — но лес всегда встаёт на его сторону.`,
      n => `Там, где ступает ${n}, мёртвое дерево вновь пускает побеги.`,
      n => `${n} — последний страж чащи, куда не заходят даже волки.`,
    ],
    electric: [
      n => `${n} рождается в момент, когда молния бьёт в сухую землю.`,
      n => `Перед появлением ${n} воздух всегда пахнет грозой.`,
      n => `${n} движется быстрее, чем успевает прогреметь гром.`,
      n => `Шрамы на теле ${n} светятся перед каждой бурей.`,
      n => `Ни один клинок не выдерживает прикосновения ${n}.`,
    ],
    shadow: [
      n => `${n} не отбрасывает тени — он сам и есть чья-то забытая тень.`,
      n => `О ${n} говорят шёпотом, даже когда его нет рядом.`,
      n => `${n} питается не плотью, а страхом, что идёт впереди него.`,
      n => `Свечи гаснут за три шага до появления ${n}.`,
      n => `${n} помнит каждое имя, вычеркнутое из летописей.`,
    ],
    light: [
      n => `${n} горит ровным светом даже в самой глухой тьме подземелий.`,
      n => `Клятвы, данные перед ${n}, невозможно нарушить безнаказанно.`,
      n => `${n} явился тем, кто перестал верить, что рассвет ещё возможен.`,
      n => `Там, где проходит ${n}, тьма отступает — но ненадолго.`,
      n => `${n} несёт свет не для себя, а для тех, кто идёт следом.`,
    ],
  };

  const RARITY_TAGS = {
    common:    n => `${n} — самый обычный житель этих земель, но и у него есть своя история.`,
    uncommon:  null,
    rare:      null,
    epic:      n => `Легенды о ${n} передаются из поколения в поколение неспроста.`,
    legendary: n => `${n} — имя, которое сама история пыталась стереть, но не смогла.`,
  };

  function get(card) {
    const rng = makeRng(card.id + '#' + card.name);
    const pool = TEMPLATES[card.element] || TEMPLATES.shadow;
    const base = pick(rng, pool)(card.name);
    const tag = RARITY_TAGS[card.rarity];
    if (tag && rng() > 0.4) return tag(card.name);
    return base;
  }

  return { get };
})();
